MIN_FREQ = 2
MAX_VOCAB = 50000

def build_vocab(texts):
    counter = Counter()

    for text in texts:
        counter.update(text.split())

    vocab = {"<PAD>": 0, "<UNK>": 1}

    for word, count in counter.most_common():
        if count < MIN_FREQ or len(vocab) >= MAX_VOCAB:
            break
        vocab[word] = len(vocab)

    return vocab

