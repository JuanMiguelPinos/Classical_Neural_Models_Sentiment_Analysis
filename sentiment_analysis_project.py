def bootstrap_f1(y_true, pred, samples=500, seed=42):
    rng = np.random.default_rng(seed)
    scores = []
    size = len(y_true)

    for _ in range(samples):
        indexes = rng.integers(0, size, size)
        score = f1_score(
            y_true[indexes],
            pred[indexes],
            labels=[0, 1, 2],
            average="macro",
            zero_division=0
        )
        scores.append(score)

    return np.percentile(scores, 2.5), np.percentile(scores, 97.5)


